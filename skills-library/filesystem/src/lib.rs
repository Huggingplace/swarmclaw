use swarmclaw_sdk::{SwarmClawSkill};
use serde_json::{json, Value};
use anyhow::{Result, Context};
use std::fs;

pub struct FileSystemSkill;

impl SwarmClawSkill for FileSystemSkill {
    fn name(&self) -> &str {
        "filesystem"
    }

    fn description(&self) -> &str {
        "Read and write files within the sandboxed workspace."
    }

    fn execute(&self, args: Value) -> Result<String> {
        let method = args.get("method").and_then(|v| v.as_str()).context("Missing 'method'")?;
        
        match method {
            "read_file" => {
                let path = args.get("path").and_then(|v| v.as_str()).context("Missing 'path'")?;
                fs::read_to_string(path).context("Failed to read file")
            },
            "write_file" => {
                let path = args.get("path").and_then(|v| v.as_str()).context("Missing 'path'")?;
                let content = args.get("content").and_then(|v| v.as_str()).context("Missing 'content'")?;
                fs::write(path, content).context("Failed to write file")?;
                Ok(format!("Successfully wrote to {}", path))
            },
            _ => anyhow::bail!("Unknown method: {}", method),
        }
    }
}

